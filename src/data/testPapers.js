// Helper function to extract analytics data from paper structure and title
export function extractPaperAnalytics(paperPath, paperTitle) {
  // paperPath example: "primary.p5.english.wa1" or "secondary.o_level.A_math.sa2"
  const pathParts = paperPath.split('.');
  
  // Extract level (primary/secondary)
  const level = pathParts[0];
  
  // Extract year and subject from title
  const yearMatch = paperTitle.match(/(\d{4})/);
  const year = yearMatch ? yearMatch[1] : '2024';
  
  // Extract subject from path
  let subject;
  if (level === 'primary') {
    subject = pathParts[2]; // english, math, etc.
  } else {
    subject = pathParts[2]; // A_math, etc.
  }
  
  return {
    level,
    subject,
    year
  };
}